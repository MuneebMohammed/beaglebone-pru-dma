/* 1D Transfer Parameters */
typedef struct {
	uint32_t src;
	uint32_t dst;
	uint32_t chan;
	uint32_t slot;
	uint16_t size;
} edma_data;

#define EDMA0_CC_BASE	((volatile uint32_t *)(0x49000000))

void edma_setup(volatile uint32_t *edma_ptr, edma_data *edma_buf);
void edma_trigger(volatile uint32_t *edma_ptr, edma_data *edma_buf);
